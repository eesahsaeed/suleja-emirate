const fs = require('fs');
const path = require('path');
const cheerio = require('cheerio');

const projectRoot = path.resolve(__dirname, '..');
const oldRoot = path.resolve(projectRoot, '..', 'suleja_emirate_old');
const outputDir = path.join(projectRoot, 'src', 'data');
const outputFile = path.join(outputDir, 'legacyPages.js');
const modulesDir = path.join(outputDir, 'legacyPagesModules');
const assetsRoot = path.join(projectRoot, 'public', 'legacy-assets');
const videoExtensions = new Set(['.mp4', '.webm', '.ogg', '.mov', '.m4v', '.avi', '.wmv', '.flv', '.mkv', '.3gp']);

function ensureDir(dirPath) {
  fs.mkdirSync(dirPath, { recursive: true });
}

function toPosix(filePath) {
  return filePath.split(path.sep).join('/');
}

function cleanText(value) {
  return (value || '').replace(/\s+/g, ' ').trim();
}

function createModuleFileBase(fileName) {
  return path
    .basename(fileName, '.html')
    .toLowerCase()
    .replace(/&/g, ' and ')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .replace(/-{2,}/g, '-') || 'page';
}

function writeLegacyPageModules(pages) {
  ensureDir(modulesDir);

  const staleModuleFiles = fs.readdirSync(modulesDir).filter((name) => name.toLowerCase().endsWith('.js'));

  staleModuleFiles.forEach((name) => {
    fs.unlinkSync(path.join(modulesDir, name));
  });

  const moduleImports = [];
  const moduleItems = [];
  const usedModuleFileBases = new Set();

  pages.forEach((page, pageIndex) => {
    const importVar = `legacyPage${String(pageIndex + 1).padStart(3, '0')}`;
    const baseModuleFileBase = createModuleFileBase(page.fileName);
    let moduleFileBase = baseModuleFileBase;
    let suffix = 2;

    while (usedModuleFileBases.has(moduleFileBase)) {
      moduleFileBase = `${baseModuleFileBase}-${suffix}`;
      suffix += 1;
    }

    usedModuleFileBases.add(moduleFileBase);

    const moduleFileName = `${moduleFileBase}.js`;
    const modulePath = path.join(modulesDir, moduleFileName);
    const moduleSource = `/* Auto-generated by scripts/generate-legacy-pages.cjs */\n` +
      `/* Source: ${toPosix(oldRoot)} */\n` +
      `/* Legacy file: ${page.fileName} */\n\n` +
      `const legacyPage = ${JSON.stringify(page, null, 2)};\n\n` +
      `export default legacyPage;\n`;

    fs.writeFileSync(modulePath, moduleSource, 'utf8');

    moduleImports.push(`import ${importVar} from './legacyPagesModules/${moduleFileBase}';`);
    moduleItems.push(`  ${importVar}`);
  });

  const indexSource = `/* Auto-generated by scripts/generate-legacy-pages.cjs */\n` +
    `/* Source: ${toPosix(oldRoot)} */\n\n` +
    `${moduleImports.join('\n')}\n\n` +
    `const legacyPages = [\n${moduleItems.join(',\n')}\n];\n\n` +
    `export default legacyPages;\n`;

  fs.writeFileSync(outputFile, indexSource, 'utf8');
}

function listFilesRecursively(directory) {
  const entries = fs.readdirSync(directory, { withFileTypes: true });
  const files = [];

  entries.forEach((entry) => {
    const absolute = path.join(directory, entry.name);
    if (entry.isDirectory()) {
      files.push(...listFilesRecursively(absolute));
      return;
    }

    if (entry.isFile()) {
      files.push(absolute);
    }
  });

  return files;
}

function listLegacyVideos() {
  return listFilesRecursively(oldRoot)
    .filter((absolutePath) => videoExtensions.has(path.extname(absolutePath).toLowerCase()))
    .map((absolutePath) => toPosix(path.relative(oldRoot, absolutePath)))
    .sort((left, right) => left.localeCompare(right));
}

function guessVideoMime(relativePath) {
  const extension = path.extname(relativePath).toLowerCase();
  if (extension === '.mp4' || extension === '.m4v') return 'video/mp4';
  if (extension === '.webm') return 'video/webm';
  if (extension === '.ogg') return 'video/ogg';
  if (extension === '.mov') return 'video/quicktime';
  if (extension === '.avi') return 'video/x-msvideo';
  if (extension === '.wmv') return 'video/x-ms-wmv';
  if (extension === '.flv') return 'video/x-flv';
  if (extension === '.mkv') return 'video/x-matroska';
  if (extension === '.3gp') return 'video/3gpp';
  return 'video/mp4';
}

function humanizeFileName(fileName) {
  return path
    .basename(fileName, '.html')
    .replace(/[_-]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

function createBaseSlug(fileName) {
  return path
    .basename(fileName, '.html')
    .toLowerCase()
    .replace(/&/g, ' and ')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .replace(/-{2,}/g, '-') || 'page';
}

function isExternalReference(value) {
  if (!value) return true;
  return (
    value.startsWith('http://') ||
    value.startsWith('https://') ||
    value.startsWith('//') ||
    value.startsWith('mailto:') ||
    value.startsWith('tel:') ||
    value.startsWith('data:') ||
    value.startsWith('#')
  );
}

function normalizeLocalReference(value) {
  if (!value) return '';
  let normalized = value.trim();

  if (!normalized || isExternalReference(normalized)) {
    return normalized;
  }

  normalized = normalized.replace(/\\/g, '/');
  normalized = normalized.split('#')[0].split('?')[0];
  normalized = normalized.replace(/^\.\//, '');
  normalized = normalized.replace(/^\//, '');

  try {
    normalized = decodeURIComponent(normalized);
  } catch (error) {
    // Keep the original normalized value when URI decoding fails.
  }

  return normalized;
}

function buildSlugMaps(htmlFiles) {
  const slugByFile = {};
  const slugByFileLower = {};
  const usedSlugs = new Set();

  for (const fileName of htmlFiles) {
    const baseSlug = createBaseSlug(fileName);
    let slug = baseSlug;
    let suffix = 2;

    while (usedSlugs.has(slug)) {
      slug = `${baseSlug}-${suffix}`;
      suffix += 1;
    }

    usedSlugs.add(slug);
    slugByFile[fileName] = slug;
    slugByFileLower[fileName.toLowerCase()] = slug;
  }

  return { slugByFile, slugByFileLower };
}

function generateLegacyPages() {
  if (!fs.existsSync(oldRoot)) {
    throw new Error(`Old site folder not found at: ${oldRoot}`);
  }

  const htmlFiles = fs
    .readdirSync(oldRoot)
    .filter((name) => name.toLowerCase().endsWith('.html'))
    .sort((a, b) => a.localeCompare(b));

  const { slugByFile, slugByFileLower } = buildSlugMaps(htmlFiles);
  const discoveredVideoFiles = listLegacyVideos();
  const primaryMovieFallback = discoveredVideoFiles.find(
    (videoPath) => path.posix.basename(videoPath).toLowerCase() === 'movie.mov'
  );
  const copiedAssets = new Set();
  const missingAssets = new Set();
  const pages = [];

  function resolveFallbackAssetPath(cleaned) {
    const normalized = cleaned.toLowerCase();

    if (normalized.includes('movie..mov')) {
      const fixed = cleaned.replace(/movie\.\.mov/gi, 'movie.MOV');
      const fixedSourcePath = path.resolve(oldRoot, fixed);
      if (fixedSourcePath.startsWith(oldRoot) && fs.existsSync(fixedSourcePath) && fs.statSync(fixedSourcePath).isFile()) {
        return normalizeLocalReference(fixed);
      }
    }

    const baseName = path.posix.basename(normalized);
    if (['movie.mp4', 'movie.ogg', 'movie.webm', 'movie.mov', 'movie..mov'].includes(baseName) && primaryMovieFallback) {
      return primaryMovieFallback;
    }

    return '';
  }

  function copyAssetAndReturnPublicPath(relativePath) {
    const cleaned = normalizeLocalReference(relativePath);

    if (!cleaned || isExternalReference(cleaned)) {
      return relativePath;
    }

    let resolvedRelativePath = cleaned;
    let sourcePath = path.resolve(oldRoot, resolvedRelativePath);

    if (!sourcePath.startsWith(oldRoot)) {
      return relativePath;
    }

    if (!fs.existsSync(sourcePath) || !fs.statSync(sourcePath).isFile()) {
      const fallbackPath = resolveFallbackAssetPath(cleaned);
      if (!fallbackPath) {
        missingAssets.add(cleaned);
        return relativePath;
      }

      resolvedRelativePath = fallbackPath;
      sourcePath = path.resolve(oldRoot, resolvedRelativePath);
      if (!sourcePath.startsWith(oldRoot) || !fs.existsSync(sourcePath) || !fs.statSync(sourcePath).isFile()) {
        missingAssets.add(cleaned);
        return relativePath;
      }
    }

    const destinationPath = path.join(assetsRoot, resolvedRelativePath);
    ensureDir(path.dirname(destinationPath));

    if (!copiedAssets.has(resolvedRelativePath)) {
      const shouldCopy =
        !fs.existsSync(destinationPath) ||
        fs.statSync(destinationPath).size !== fs.statSync(sourcePath).size;

      if (shouldCopy) {
        fs.copyFileSync(sourcePath, destinationPath);
      }
      copiedAssets.add(resolvedRelativePath);
    }

    return `/legacy-assets/${toPosix(resolvedRelativePath)}`;
  }

  for (const fileName of htmlFiles) {
    const absolutePath = path.join(oldRoot, fileName);
    const html = fs.readFileSync(absolutePath, 'utf8');
    const $ = cheerio.load(html, { decodeEntities: false });

    const mainSelectors = [
      'section#content',
      'section.content-section',
      'section.carousel-section',
      '#content',
      '#wrapper',
      'body',
      'section#inner-headline'
    ];

    let rootNode = null;

    for (const selector of mainSelectors) {
      const candidate = $(selector).first();
      if (candidate.length === 0) {
        continue;
      }

      const candidateText = cleanText(candidate.text());
      if (candidateText.length > 40 || selector === 'body') {
        rootNode = candidate.clone();
        break;
      }
    }

    if (!rootNode) {
      rootNode = $('body').first().clone();
    }

    rootNode.find('script, style, link, meta, noscript, header, footer').remove();
    rootNode.find('.navigation, .navbar, .scrollup, .social-network, .topmenu').remove();

    rootNode.find('*').each((_, element) => {
      const attribs = element.attribs || {};
      for (const attributeName of Object.keys(attribs)) {
        if (attributeName.toLowerCase().startsWith('on')) {
          rootNode.find(element).removeAttr(attributeName);
        }
      }
    });

    rootNode.find('[src]').each((_, element) => {
      const src = $(element).attr('src');
      if (!src) {
        return;
      }

      const updated = copyAssetAndReturnPublicPath(src);
      $(element).attr('src', updated);
    });

    rootNode.find('[href]').each((_, element) => {
      const href = $(element).attr('href');
      if (!href) {
        return;
      }

      const cleanedHref = normalizeLocalReference(href);
      if (!cleanedHref || isExternalReference(cleanedHref)) {
        return;
      }

      if (cleanedHref.toLowerCase().endsWith('.html')) {
        const legacyFileName = path.basename(cleanedHref).toLowerCase();
        const mappedSlug = slugByFileLower[legacyFileName];
        if (mappedSlug) {
          $(element).attr('href', `/pages/${mappedSlug}`);
        }
        return;
      }

      const updatedHref = copyAssetAndReturnPublicPath(cleanedHref);
      $(element).attr('href', updatedHref);
    });

    if (fileName.toLowerCase() === 'v.html' && discoveredVideoFiles.length > 0) {
      const videoCards = discoveredVideoFiles
        .map((videoPath, index) => {
          const publicPath = copyAssetAndReturnPublicPath(videoPath);
          const mime = guessVideoMime(videoPath);
          const label = cleanText(humanizeFileName(videoPath)) || `Video ${index + 1}`;
          return `
            <article class="legacy-video-item">
              <h5>${label}</h5>
              <video controls preload="metadata">
                <source src="${publicPath}" type="${mime}">
                Your browser does not support the video tag.
              </video>
            </article>
          `;
        })
        .join('\n');

      rootNode.append(`
        <div class="legacy-video-archive">
          <h4>Video Archive</h4>
          <p>Original local videos from the legacy site.</p>
          <div class="legacy-video-grid">
            ${videoCards}
          </div>
        </div>
      `);
    }

    const titleCandidates = [
      cleanText(rootNode.find('h1').first().text()),
      cleanText(rootNode.find('h2').first().text()),
      cleanText(rootNode.find('h3').first().text()),
      cleanText($('title').first().text()),
      humanizeFileName(fileName)
    ];

    const title = titleCandidates.find((candidate) => candidate.length > 0) || humanizeFileName(fileName);
    const textSummary = cleanText(rootNode.text());
    const summary =
      textSummary.length > 220 ? `${textSummary.slice(0, 217).trimEnd()}...` : textSummary;

    pages.push({
      slug: slugByFile[fileName],
      fileName,
      title,
      summary,
      contentHtml: rootNode.html() || ''
    });
  }

  discoveredVideoFiles.forEach((videoPath) => {
    copyAssetAndReturnPublicPath(videoPath);
  });

  ensureDir(outputDir);
  ensureDir(assetsRoot);
  writeLegacyPageModules(pages);

  return {
    pageCount: pages.length,
    copiedAssetCount: copiedAssets.size,
    missingAssetCount: missingAssets.size,
    missingAssets: Array.from(missingAssets).sort()
  };
}

try {
  const result = generateLegacyPages();
  console.log(`Generated ${result.pageCount} pages.`);
  console.log(`Copied ${result.copiedAssetCount} local assets.`);
  console.log(`Missing assets: ${result.missingAssetCount}.`);
  if (result.missingAssetCount > 0) {
    const preview = result.missingAssets.slice(0, 25);
    console.log('Missing asset examples:');
    preview.forEach((asset) => console.log(`- ${asset}`));
    if (result.missingAssets.length > preview.length) {
      console.log(`...and ${result.missingAssets.length - preview.length} more.`);
    }
  }
} catch (error) {
  console.error(error.message);
  process.exit(1);
}
